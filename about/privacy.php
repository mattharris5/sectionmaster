	  <h2>Security</h2>

<h4>How we protect the security of your personal information</h4>

<p>The security of your personal information is very important to us, and we
are committed to protecting the information we collect. The SectionMaster.org
site uses firewalls and Secure Socket Layer ("SSL") to transport all personal data we collect
from you. We also employ many different security techniques to protect personal
data against loss, misuse, alteration, and unauthorized access. But we cannot
guarantee perfect security of your personal information. </p>

<p>When you send confidential personal information to us on our
website, such as credit card information, member name, or user ID and passkey,
secure server software that we have licensed encrypts all information you input
before it is sent to us.  The information is scrambled en route and decoded
once it reaches our website using Secure Socket Layers (SSL) and the Secure
Hypertext Transfer Protocol (HTTPS).  By default, when signed on or making a
payment, all communication with us is encrypted.</p>

	  <p>Secure communication can be verified by clicking on your web browser's security 
		seal, often appearing as a padlock. The SectionMaster.org site utilizes 
		"Shared-SSL" technology offered by its web provider, WestHost, Inc. The 
		secure certificate is issued by <a href="http://thawte.com" target="_blank">Thawte, 
		Inc</a>. SectionMaster URLs, when in secure mode, will appear as they 
		are running at https://ssl4.westserver.net/sectionmaster.org, but this 
		is normal and can be trusted to be as secure as any other secure website.</p>

<p>We also use encrypted communications to transmit information
between us and our service providers.  For example, to process credit card
payments we have a secure connection to our payment processor.</p>

<p>For software, secure communications, and service providers
we only use well-known and established companies generally recognized a leader
in their field.  For example, we use Elavon and Authorize.net for payment processing, the
most reliable payment processor in the industry.</p>

<p>We do not store or maintain highly sensitive information. 
This information includes social security and credit card numbers.  Where we
are required to collect this information to fulfill a product or service, we
immediately pass it on to the service provider.</p>

<p>To safeguard the confidentiality of personal information we
collect from unauthorized access or disclosure our servers are housed behind a
firewall in secure, restricted access facilities with 24-hour surveillance
provided by WestHost, Inc.  Only named and authorized representatives of
WestHost, Inc. can physically access our servers.  Detailed information about
our web provider's facilities can be found at <a
href="http://www.westhost.com/datacenter.html" target="_blank">http://www.westhost.com/datacenter.html</a>.</p>

