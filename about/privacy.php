		of 13, and SectionMaster does not knowingly collect personal information 
		from those children, per the Children's Online Privacy Protection Act 
		(COPPA).&nbsp; Collection of any information about children under the 
		age of 13 is only permitted to be sent by the child's parent or legal 
		guardian; such a child may not submit any information on his or her own 
		behalf without assistance.&nbsp; A child's age is verified with a consent 
		statement upon entering the child's date of birth on the initial entry 
		page(s) of the SectionMaster.org site.</p>

	  <h2>Applicability of this privacy policy</h2>
<p>This privacy policy applies only to the information that we collect on the
SectionMaster.org site and not to any information collected through other
methods or sources, including sites owned or operated organizations contracted
with SectionMaster. When visiting other Web sites that are not owned or
operated by SectionMaster, please review the privacy policy for those sites.</p>

	  <h2>Notice</h2>

<h4>What personal information we collect and how we use it</h4>

<p>While you can browse our site without providing personal information, you
will need to input certain personal information as indicated on specific pages
of our site in order to take advantage of certain features, such as registering
for events or purchasing merchandise.  Use of the SectionMaster.org site is voluntary,
and your use constitutes your consent for the information provided to be stored
and used by SectionMaster in order for SectionMaster to fulfill its contracted
agreements to provide event registration and other similar services.</p>

<p>Only authorized organization administrators are permitted to
view information stored in the organization's database.  The list of
administrators is determined at the time the organization signs the Usage
Agreement.  Administrative access can be requested or revoked by the original
signer of the agreement or his supervisor.  SectionMaster reserves the right to
deny access to any administrator in the event suspicious activity is observed
that may compromise the security and integrity of personal information stored
on the SectionMaster.org site.</p>

<h4>Browsing and the use of cookies and logs</h4>

<p>When you visit the SectionMaster.org site we use "cookies"
to store your user session while you are logged on to our website.  A cookie is
a small amount of data that is transferred to your browser by a web server and
can only be read by the server that gave it to you.  It cannot be executed as
code or deliver viruses.</p>

<p>Most browsers are initially set to accept cookies.  You can
set your browser to notify you when you receive a cookie, giving you the chance
to decide whether to accept it.  Cookies are required to use the
SectionMaster.org site.</p>

<p>While the SectionMaster.org Web servers automatically log
the IP/Internet address of your computer, SectionMaster does not use this
information other than to protect ourselves and our members against fraud and
illegal activity.  SectionMaster does not use cookies to "remember" the user
logon credentials you use to sign on to the site.  The cookies we use are
"session only" cookies and are removed when your visit to our site ends.</p>

<p>We also use standard Web server log files to help us count visitors and
evaluate our site's technical capacity. This information tells us how many
people visit the SectionMaster.org site, and helps us decide how to arrange
pages in the most user-friendly way. It also helps us keep the site
browser-friendly and more useful to our visitors. We record information on site
traffic, but not on individual visitors to our site, so no personal information
about you will be kept or used.</p>

<h4>Email, mailing lists, and other services</h4>

<p>When you send us an email, we use your email address to thank you for your
comment and/or reply to your question. We store your communication and our
reply in case we correspond further. Beyond our initial reply, we will not use
your email address to send you any unsolicited message or information, nor will
we share it with, or sell it to, anyone else for such use, other than as
specified in this policy. </p>

<p>When you request information or services, we use your email address and any
other information you give us to provide you with the information or services
you've requested until you ask us to stop (by using "unsubscribe" instructions
provided on each email and/or the Web site where you signed up and/or as we
otherwise provide), or until the information or service is no longer available.
We will not use your email address or other information to provide you with any
unsolicited message or information (unless the message or information is part
of the service you are requesting). </p>

<h4>Links to other sites</h4>

<p>We work closely with our contracted partners, many of which have links
within the SectionMaster.org site. Please remember that when you use a link to
go from the SectionMaster.org site to another Web site, this Privacy Policy no
longer applies. Your browsing and interaction on any third party's Web site,
including any site that has a link on the SectionMaster.org site, is subject to
the rules and policies of that site. We encourage you to read the rules and
policies of the sites you visit to better understand their procedures for
collecting, using, and disclosing personal information. </p>

<h4>Our policy regarding selling or renting of personal information</h4>

<p>Our policy is simple: we do not sell or rent your personal information to
any third parties under any circumstances, except in the event of a bankruptcy
