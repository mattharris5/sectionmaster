	  <h1>SectionMaster Privacy Policy</h1>

<p>Any time you visit a Web site, you may be providing information about
yourself to the site's owner or operator. We at SectionMaster, like you, value
privacy, so we want you to learn what you need to know to protect your personal
privacy. When you visit or supply information to any Web site, it's a good idea
for you to first understand the site's privacy policies. If the site owner
doesn't tell you, don't hesitate to ask. </p>

<p><b>The bottom line</b> at our Web site
(also referred to generally as the &quot;SectionMaster.org site&quot;) is that
we use information that we get from your visit to enable you to participate in
and take advantage of the various programs and features presented on the
SectionMaster.org site and to make the SectionMaster.org site useful and
interesting to you. We do not use the information that we collect from the
SectionMaster.org site for any other purpose, nor do we share it with any
affiliate or third party, except those specifically under contract with
SectionMaster to collect information for primary uses only.</p>

	  <h2>A special note about children</h2>
